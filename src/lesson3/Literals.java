package lesson3;

public class Literals {
    public static void main(String[] args) {

        System.out.println("Boolean: " + true);
        System.out.println("string:" + "abcd");
        System.out.println("char: " + 'a');
        System.out.println("int2: " + 0b101);
        System.out.println("int8: " + 011);
        System.out.println("int10:" + 125);
        System.out.println("int16: " + 0x7ddfff5);
        System.out.println("float: " + 125.365F);
        System.out.println("double: " + 125.3654);
    }
}
